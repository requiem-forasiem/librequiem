package GenerateIDMEFMessageIdH;

use Generate;
@ISA = qw/Generate/;

use strict;
use IDMEFTree;

sub	header
{
     my	$self = shift;
     my	$tree = shift;
     my	$cnt = 0;

     $self->output("
/* Auto-generated by the GenerateIDMEFMessageIdH package */

#ifndef _LIBREQUIEM_IDMEF_MESSAGE_ID_H
#define _LIBREQUIEM_IDMEF_MESSAGE_ID_H

/*
 * Misc value
 */

#define IDMEF_MSG_OWN_FORMAT                                 253
#define IDMEF_MSG_END_OF_TAG                                 254

/*
 * Tag value
 */

");

     foreach my $struct ( @{ $tree->{struct_list} } ) {
	 if ( ! $struct->{toplevel} ) {
	     $self->output("#define ", "IDMEF_MSG_" . uc($struct->{short_typename}) . "_TAG ", $cnt++, "\n");
	 }
     }

     $self->{max_tag_value} = $cnt;

     $self->output("\n\n");
}

sub	footer
{
    my	$self = shift;

    $self->output("#endif /* _LIBREQUIEM_IDMEF_MESSAGE_ID_H */", "\n");
}

sub	struct
{
    my	$self = shift;
    my	$tree = shift;
    my	$struct = shift;
    my	$cnt = $self->{max_tag_value};

    $self->output("/*\n",
		  " * IDMEF \u$struct->{short_typename}\n",
		  " */\n");

    foreach my $field ( @{ $struct->{field_list} } ) {

	if ( $field->{metatype} & (&METATYPE_PRIMITIVE | &METATYPE_ENUM) ) {
	    $self->output("#define ", "IDMEF_MSG_", uc($struct->{short_typename}), "_", uc($field->{short_name}), " ", $cnt, "\n");
	    $cnt++;
	}
    }

    $self->output("\n\n");
}

1;
